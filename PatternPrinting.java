package Java;

public class PatternPrinting {
    
    public static void main(String[] args) {
        
       for(int i=0;i<5;i++){
        for(int j=0;j<5;j++){
            System.out.println("$");
        }
       }

       System.out.println("Pattern printing is nice, Pattern printing helps build logic");

       for(int j=0;j<10;j++){
        System.out.println("great");
       }

       
    }
}
